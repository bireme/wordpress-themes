                um sonho e uma necessidade coletiva de constituir sistemas nacionais de informação científica que reunissem e dessem visibilidade
                à pesquisa em saúde produzida na região.
              </p>
              <p>
                Em <strong>1985</strong>, surge oficialmente a <strong>LILACS</strong>, com uma metodologia própria de gestão da informação e uma atuação em rede,
                descentralizada e cooperativa, envolvendo coordenadores e centros cooperantes em cada país. De forma pioneira, incorporou tecnologias como
                o CD-ROM, as primeiras interfaces de pesquisa via WWW e, hoje, avança na aplicação de inteligência artificial em processos de indexação e geração de resumos.
              </p>
              <p>
                A Rede LILACS tem um papel fundamental: mais de <strong>900 instituições</strong> em <strong>30 países</strong> mantêm viva essa rede coordenada pela
                <strong>BIREME/OPAS/OMS</strong>, consolidando décadas de cooperação para ampliar o acesso, a qualidade e o intercâmbio da informação científica em saúde.
              </p>
              <p>
                A LILACS deu origem ao ecossistema de informação científica com centenas de bases de dados coordenadas pela BVS e foi plataforma de origem do SciELO,
